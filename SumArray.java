package arrays;

import java.util.Scanner;

public class SumArray {
public static void main(String[] args) {
		
		
		int[] array = {1, 5, 3, 7, 2};
		
		int sum = 0;
		
		for (int i = 0; i < array.length; i++) {
			
			sum += array[i];
			
		}
		
	
			System.out.println(sum);
		
		

	}

}
