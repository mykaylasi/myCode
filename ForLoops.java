package newInClass;

public class ForLoops {
	public static void main(String[] args) {
		
		//for (section1; section2; section3) { }
		//for (exp1; bool_exp; exp2;) { }
		
		for (int i = 0; i < 10; i++) {
			System.out.println("Hello");
			// you got first block
			// i = 0
			// is that less than 10?
			// yes! do the statements.
			// done doing the statements?
			// go to the last block
			// increase
			// go back to the second block
			// is it less than 10?
			// keep on til_ you get to 10
			// its over wit_
			// NOTE: you only touch the first section once! just that first time!
			
			// all sections can be dropped! even altogether!
			// for ( ; ; ) { } is valid! it is the same thing as for ( ; true; ) { }
			
		}
		
		// convert to a WHILE loop 
		
		int i = 0;
		
		while (i < 10) {
			System.out.println("Hello");
			i++;
		}
	}

}
